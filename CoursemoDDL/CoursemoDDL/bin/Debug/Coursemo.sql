CREATE TABLE Students (
	ID INT IDENTITY(20001, 1) PRIMARY KEY,
	FirstName NVARCHAR(128) NOT NULL,
	LastName NVARCHAR(128) NOT NULL,
	Netid NVARCHAR(128) NOT NULL
);

CREATE TABLE Courses (
	CRN INT PRIMARY KEY,
	Department NVARCHAR(12) NOT NULL,
	Number NVARCHAR(12) NOT NULL,
	_Type NVARCHAR(12) NOT NULL,
	_Year NVARCHAR(12) NOT NULL,
	_Semester NVARCHAR(12) NOT NULL,
	_Day NVARCHAR(12) NOT NULL,
	_Time NVARCHAR(12) NOT NULL,
	Size INT NOT NULL,
	Available INT NOT NULL
);

CREATE TABLE Waitlist (
	WLID INT IDENTITY(1, 1) PRIMARY KEY,
	ID INT NOT NULL FOREIGN KEY REFERENCES Students(ID),
	CRN INT NOT NULL FOREIGN KEY REFERENCES Courses(CRN)
);

CREATE TABLE Enrolled (
	ELID INT IDENTITY(1, 1) PRIMARY KEY,
	ID INT NOT NULL FOREIGN KEY REFERENCES Students(ID),
	CRN INT NOT NULL FOREIGN KEY REFERENCES Courses(CRN)
);